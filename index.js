$('#fullscreen').click(function (e) {
    
    $('#div-to-expand').toggleClass('fullscreen');
    $('#div-to-expand').hasClass("fullscreen") ? $('#btn1').attr("src","minimize.svg") : $('#btn1').attr('src',"maximize.svg");
});

